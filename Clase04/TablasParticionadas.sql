----------------------------------
-- 1. CREACION DE TBS
----------------------------------

CREATE TABLESPACE TBS_ASIA
DATAFILE 'E:\APP\TS\DF_COMPROBANTES_ASIA.DBF'
SIZE 10M;

CREATE TABLESPACE TBS_EUROPA
DATAFILE 'E:\APP\TS\DF_COMPROBANTES_EUROPA.DBF'
SIZE 10M;

CREATE TABLESPACE TBS_AL
DATAFILE 'E:\APP\TS\DF_COMPROBANTES_AL.DBF'
SIZE 10M;
  
CREATE TABLESPACE TBS_OTROS
DATAFILE 'E:\APP\TS\DF_COMPROBANTES_OTROS.DBF'
SIZE 10M;

-- Tablespaces
SELECT TABLESPACE_NAME "TABLESPACE"	
FROM DBA_TABLESPACES 
WHERE TABLESPACE_NAME LIKE 'TBS%'
ORDER BY 1;

-- Espacio Total en MB
SELECT 
	tablespace_name,
	round(sum(BYTES/1024/1024),0) ESPACIO_MB
FROM dba_data_files b
WHERE b.TABLESPACE_NAME LIKE 'TBS%'
GROUP BY b.tablespace_name
ORDER BY 1;

-- Espacio libre en MB
SELECT 
	tablespace_name,
	ROUND(sum(BYTES/1024/1024),0) ESPACIO
FROM dba_free_space
WHERE TABLESPACE_NAME LIKE 'TBS%'
GROUP BY tablespace_name
ORDER BY 1;



----------------------------------
-- 2. TABLA PARTICIONADA POR LISTA
----------------------------------

CREATE TABLE SCOTT.Ventas(
	ID NUMBER(10),
	ORIGEN VARCHAR2(20),
	FECHA DATE default sysdate 
)
PARTITION BY LIST( ORIGEN)
(
	PARTITION ventas_ASIA  VALUES('ASIA')     tablespace TBS_ASIA,
	PARTITION ventas_EUROPA VALUES ('EUROPA') tablespace TBS_EUROPA,
	PARTITION ventas_AL     VALUES ('AL')     tablespace TBS_AL,
	PARTITION ventas_otros  VALUES(DEFAULT)   tablespace TBS_OTROS 
);


-------------------------------------------------
-- 3 INSERTANDO DATOS EN TABLAS PARTICIONADAS
-------------------------------------------------

-- Espacio libre en MB
SELECT 
  tablespace_name,
  ROUND(sum(BYTES/1024/1024),0) ESPACIO
FROM dba_free_space
WHERE TABLESPACE_NAME LIKE 'TBS%'
GROUP BY tablespace_name
ORDER BY 1;

