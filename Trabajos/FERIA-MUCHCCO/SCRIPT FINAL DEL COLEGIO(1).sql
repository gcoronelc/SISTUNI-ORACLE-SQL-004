--CREACION DE TABLAS

CREATE TABLE PERSONA(
IDPERSONA INT NOT NULL CONSTRAINT PK_PERSONA PRIMARY KEY,
TIPOPERSONAA VARCHAR2(50) NOT NULL,
NOMBRE VARCHAR2(50) NOT NULL,
APELLIDO VARCHAR2(50) NOT NULL,
SEXO VARCHAR2(10),
FECHANACIMIENTO VARCHAR2(50),
TELEFONO VARCHAR2(11),
DIRECCION VARCHAR2(50),
EMAIL VARCHAR2(50),
EDAD INT 
);

CREATE TABLE ALUMNO
(
IDALUMNO INT NOT NULL CONSTRAINT PK_ALUMNO PRIMARY KEY,
ESTADO VARCHAR2(50) NOT NULL,
IDAPODERADO INT,
CONSTRAINT FK_ALUMNO FOREIGN KEY (IDALUMNO) REFERENCES PERSONA(IDPERSONA),
CONSTRAINT FK_ALUMNO1 FOREIGN KEY (IDAPODERADO) REFERENCES PERSONA(IDPERSONA)
);

CREATE TABLE MAESTRO
(
IDMAESTRO INT NOT NULL,
ESPECIALIDAD VARCHAR2(50) NOT NULL,
SUELDO  NUMBER  NOT NULL,
ESTADO VARCHAR2(10) NOT NULL,
FECHAINGRESO VARCHAR2(50) NOT NULL,
CONSTRAINT PK_MAESTRO PRIMARY KEY (IDMAESTRO),
CONSTRAINT FK_MAESTRO FOREIGN KEY (IDMAESTRO) REFERENCES PERSONA(IDPERSONA)
);

CREATE TABLE GRADO
(
CODGRADO VARCHAR2(50) NOT NULL,
GRADO NUMBER NOT NULL,
NIVEL VARCHAR(50) NOT NULL,
PISO NUMBER NOT NULL,
CONSTRAINT PK_GRADO PRIMARY KEY (CODGRADO)
);

CREATE TABLE ASIGNATURA
(
CODASIGNATURA VARCHAR2(50) NOT NULL,
NOMB_ASIGNATURA VARCHAR(50) NOT NULL,
CONSTRAINT  PK_ASIGNATURA PRIMARY KEY (CODASIGNATURA)
);

CREATE TABLE GRADO_ASIGNATURA
(
CODGRADO VARCHAR2(50) NOT NULL,
CODASIGNATURA VARCHAR2(50) NOT NULL,
CONSTRAINT FK_GRADO FOREIGN KEY (CODGRADO) REFERENCES GRADO(CODGRADO),
CONSTRAINT FK_ASIGNATURA FOREIGN KEY (CODASIGNATURA) REFERENCES ASIGNATURA(CODASIGNATURA)
);

CREATE TABLE MAESTRO_ASIGNATURA
(
IDMAESTRO INT NOT NULL,
CODASIGNATURA VARCHAR2(50) NOT NULL,
ESTADO VARCHAR2(50) NOT NULL,
A헲ACADEMICO VARCHAR2(50) NOT NULL,
CONSTRAINT FK_MAESTRO_ASIGNATURA FOREIGN KEY (IDMAESTRO) REFERENCES MAESTRO(IDMAESTRO),
CONSTRAINT FK_MAESTRO_ASIGNATURA1 FOREIGN KEY (CODASIGNATURA) REFERENCES ASIGNATURA(CODASIGNATURA)
);

CREATE TABLE NOTAS
(
IDALUMNO INT NOT NULL,
CODASIGNATURA VARCHAR2(50) NOT NULL,
PC1 DECIMAL(4,2),
PC2 DECIMAL(4,2),
PC3 DECIMAL(4,2),
EX_MENSUAL DECIMAL(4,2),
EX_BIMESTRAL  DECIMAL(4,2),
CONSTRAINT FK_NOTAS FOREIGN KEY (CODASIGNATURA) REFERENCES ASIGNATURA(CODASIGNATURA),
CONSTRAINT FK_NOTAS1 FOREIGN  KEY (IDALUMNO) REFERENCES ALUMNO(IDALUMNO)
);



